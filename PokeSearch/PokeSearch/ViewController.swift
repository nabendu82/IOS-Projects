//                let anno = PokeAnnotation(coordinate: location.coordinate, pokemonNumber: Int(key)!)
//                self.mapView.addAnnotation(anno)
//            }
//        })
        
    }



    @IBAction func spotRandomPokemon(_ sender: Any) {
        
        let loc = CLLocation(latitude: mapView.centerCoordinate.latitude, longitude: mapView.centerCoordinate.longitude)
        
        let rand = arc4random_uniform(151) + 1
        createSighting(forLocation: loc, withPokemon: Int(rand))
        
    }
    
    //Below code for Interstial ad
    
    @objc private func createAndLoadInterstitial() -> GADInterstitial? {
        interstitial = GADInterstitial(adUnitID: "ca-app-pub-8060048775507010/9018595089")
        
        guard let interstitial = interstitial else {
            return nil
        }
        
        let request = GADRequest()
        //request.testDevices = [ kGADSimulatorID,"a05c9dc0fa81b3a9c8aba98121713915" ]
        interstitial.load(request)
        interstitial.delegate = self
        
        return interstitial
    }
    
    func interstitialDidReceiveAd(_ ad: GADInterstitial) {
        print("Interstitial loaded successfully")
        ad.present(fromRootViewController: self)
    }
    
    func interstitialDidFail(toPresentScreen ad: GADInterstitial) {
        print("Fail to receive interstitial")
    }

}

